    private void lbBtnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbBtnExcluirMouseClicked
        // Botão excluir:
        if (tbDadosUser.getSelectedRow() != -1) {
            ConexaoComBanco ccb = new ConexaoComBanco();
            Connection connection = ccb.getConnection();
            int row = tbDadosUser.getSelectedRow();
            String cell = tbDadosUser.getModel().getValueAt(row, 0).toString();
            String sql = "DELETE FROM canal_usuario WHERE id_canal_usuario = " + cell;
            try {
                PreparedStatement stmt = connection.prepareStatement(sql);
                stmt.execute();
                JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                atualizarTabelaUser();
                connection.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
        atualizarTabelaUser();
    }//GEN-LAST:event_lbBtnExcluirMouseClicked

    private void lbBtnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbBtnEditarMouseClicked
                

        if (tbDadosUser.getSelectedRow() != -1) {

            CanalUser user = new CanalUser();
            CanalUserDAO updateDAO = new CanalUserDAO();

            user.setNome_canal(txtNomeCanalUser.getText());
            user.setToken(txtToken.getText());
            user.setUsuario(txtUsuario.getText());
            user.setSenha(txtSenha.getText());
            user.setIdusuario((int) tbDadosUser.getValueAt(tbDadosUser.getSelectedRow(), 0));

            updateDAO.updateUser(user);

            txtNomeCanalUser.setText("");
            txtSenha.setText("");
            txtUsuario.setText("");
            txtToken.setText("");

            atualizarTabelaUser();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
    }//GEN-LAST:event_lbBtnEditarMouseClicked

