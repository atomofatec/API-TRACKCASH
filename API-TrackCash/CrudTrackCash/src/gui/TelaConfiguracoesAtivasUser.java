    public boolean ObterUsuario(int idUsuario) {
        Usuario usuario = usuarioDAO.obterUsuarioPorId(idUsuario);
        if (usuario != null) {
            this.usuarioLogado = usuario;
            usuario.setId_usuario(idUsuario);
        return true;
        }else{
            return false;
        }
    }

